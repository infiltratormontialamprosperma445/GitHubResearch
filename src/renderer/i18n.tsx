interface I18nValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  windowLabel: (window: TrendWindow) => string;
  categoryLabel: (category: string) => string;
  subcategoryLabel: (subcategory: string) => string;
  statusLabel: (status: RepoStatus) => string;
}

const I18nContext = createContext<I18nValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = globalThis.localStorage?.getItem(STORAGE_KEY);
    if (saved === "zh" || saved === "en") return saved;
    return globalThis.navigator?.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
  });

  useEffect(() => {
    globalThis.localStorage?.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale === "zh" ? "zh-CN" : "en";
  }, [locale]);

  const value = useMemo<I18nValue>(() => {
    const t = (key: string, vars?: Record<string, string | number>) => {
      const template = translations[locale][key] ?? translations.en[key] ?? key;
      return Object.entries(vars ?? {}).reduce(
        (text, [name, replacement]) => text.replaceAll(`{${name}}`, String(replacement)),
        template
      );
    };

    return {
      locale,
      setLocale: setLocaleState,
      t,
      windowLabel: (window) => translations[locale][`window.${window}`] ?? translations.en[`window.${window}`] ?? window,
      categoryLabel: (category) => translations[locale][`category.${category}`] ?? translations.en[`category.${category}`] ?? category,
      subcategoryLabel: (subcategory) => translations[locale][`subcategory.${subcategory}`] ?? translations.en[`subcategory.${subcategory}`] ?? subcategory,
      statusLabel: (status) => translations[locale][`status.${status}`] ?? translations.en[`status.${status}`] ?? status
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
